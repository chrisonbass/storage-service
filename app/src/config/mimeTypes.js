export const isValidMimeType = (mimeType) => {
    let valid = false;
    for (let i = 0; i < mimeTypes.length; i++) {
        if (mimeTypes[i].mime === `${mimeType}`.trim().toLowerCase()) {
            valid = true;
            break;
        }
    }
    return valid;
};

